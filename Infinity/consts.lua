--[[
    Global constants, mostly (if not all) enums

    These are dumped from _G. This makes it easier to update.

    Lines are sorted by name. This may seem a bit scuffed, since the values are
    all over the place, but it makes it easier to diff.

    ```lua
    function DumpEnums()
        local enums = {}
        for k, v in pairs(_G) do
            table.insert(enums, {name = k, value = v})
        end

        table.sort(enums, function(a, b)
            return a.name < b.name
        end)

        local result = ''
        for _, entry in ipairs(enums) do
            local name = entry.name
            local value = entry.value
            if type(value) == 'number' or type(value) == 'boolean' then
                result = result .. name .. ' = ' .. tostring(value) .. '\n'
            elseif type(value) == 'string' then
                result = result .. name .. ' = "' .. value .. '"\n'
            end
        end

        Infinity.FileSystem.WriteFile('const__.lua', result)
    end

    DumpEnums()
    ```
